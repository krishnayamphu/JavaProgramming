package aptech.loop;

public class For {
    /**
     * for(initialization; condition; increment/decrement){
     * //statement or code to be executed
     * }
     *
     * step1: initialization
     * step2: condition
     * step3: statements
     * step4: ++/--
     */

    public static void main(String[] args) {
        for(int i=1; i<=5; i++){
            System.out.println(i); //1,2,3,4,5
        }
    }
}
